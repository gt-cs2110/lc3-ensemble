    E_BAD_TRAP:
        LEA R0, S_BAD_TRAP
        PUTS
        HALT
    S_BAD_TRAP: .stringz "\n--- Bad trap executed ---"
    
    E_BAD_INT:
        LEA R0, S_BAD_INT
        PUTS
        HALT
    S_BAD_INT: .stringz "\n--- Missing interrupt handler ---"

    TRAP_GETC:
        GETC_POLL_KBSR:
            LDI R0, KBSR
            BRzp GETC_POLL_KBSR
        LDI R0, KBDR
        RTI

    TRAP_PUTC:
        ADD R6, R6, #-1; push R0
        STR R0, R6, #0

        ; poll until display is ready
        PUTC_POLL_DSR:
            LDI R0, DSR
            BRzp PUTC_POLL_DSR
        
        LDR R0, R6, #0; pop R0
        ADD R6, R6, #1

        STI R0, DDR
        RTI

    TRAP_PUTS:
        ADD R6, R6, #-1; push R0
        STR R0, R6, #0
        ADD R6, R6, #-1; push R1
        STR R1, R6, #0

        ;; let R1 = string + i
        ;; let R0 = *(string + i)
        ADD R1, R0, #0
        PUTS_LOOP:
            LDR R0, R1, #0
            BRz PUTS_END_LOOP
            PUTC
            ADD R1, R1, #1
            BR PUTS_LOOP
        
        PUTS_END_LOOP:
        LDR R1, R6, #0; pop R1
        ADD R6, R6, #1
        LDR R0, R6, #0; pop R0
        ADD R6, R6, #1
        RTI

    TRAP_IN:
        LEA R0, S_IN_PROMPT
        PUTS
        GETC
        PUTC
        RTI
    S_IN_PROMPT: .stringz "Input character: "
        
    TRAP_PUTSP:
        ADD R6, R6, #-1; push R0
        STR R0, R6, #0
        ADD R6, R6, #-1; push R1
        STR R1, R6, #0
        ADD R6, R6, #-1; push R2
        STR R2, R6, #0
        ADD R6, R6, #-1; push R3
        STR R3, R6, #0

        ;; let R0 = next char to read
        ;; let R1 = string + i
        ;; let R2 = *(string + i)

        ADD R1, R0, #0
        PUTSP_LOOP:
            LDR R2, R1, #0 ;; load the next two characters

            ;; push first character
            LD R0, PUTSP_MASK
            AND R0, R2, R0
            BRz PUTSP_END_LOOP
            PUTC

            ;; get second character in R0
            AND R0, R0, #0
            AND R3, R3, #0
            ADD R3, R3, #8 ;; for (R3 = 8; R3 > 0; R3--)
            PUTSP_GET_HIGH_8:
                ;; let R0 = result of (R2 >> 8)
                ;; SHL algo:
                ;;   perform 8 times:
                ;;     R0 << 1
                ;;     check MSB of R2, copy the result into R0
                ;;     R2 << 1
                ADD R3, R3, #0
                BRnz PUTSP_END_GET_HIGH_8
                ADD R0, R0, R0
                ADD R2, R2, #0
                BRzp PUTSP_GH8_SHL_R2
                    ADD R0, R0, #1
                PUTSP_GH8_SHL_R2:
                ADD R2, R2, R2
                ADD R3, R3, #-1
                BR PUTSP_GET_HIGH_8

            PUTSP_END_GET_HIGH_8:
            ;; print second character
            ADD R0, R0, #0
            BRz PUTSP_END_LOOP
            PUTC

            ADD R1, R1, #1
            BR PUTSP_LOOP
        
        PUTSP_END_LOOP:
        LDR R3, R6, #0; pop R3
        ADD R6, R6, #1
        LDR R2, R6, #0; pop R2
        ADD R6, R6, #1
        LDR R1, R6, #0; pop R1
        ADD R6, R6, #1
        LDR R0, R6, #0; pop R0
        ADD R6, R6, #1
        RTI
    PUTSP_MASK: .fill x00FF

    TRAP_HALT:
        AND R7, R7, #0
        STI R7, MCR
        BR TRAP_HALT

    KBSR: .fill xFE00
    KBDR: .fill xFE02
    DSR: .fill xFE04
    DDR: .fill xFE06
    MCR: .fill xFFFE
